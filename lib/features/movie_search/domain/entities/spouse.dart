class Spouse {
  final int id;
  final String name;
  final bool divorced;
  final String divorcedReason;
  final String sex;
  final int children;
  final String relation;

  Spouse({
    required this.id,
    required this.name,
    required this.divorced,
    required this.divorcedReason,
    required this.sex,
    required this.children,
    required this.relation,
  });
}
